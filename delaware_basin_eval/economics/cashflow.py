"""
Monthly cash flow builder for both existing and undrilled wells.
"""

import numpy as np
import pandas as pd
from engineering.decline import project_monthly_volumes, fit_decline
from economics.revenue import calc_monthly_revenue
from config import MAX_PROJECTION_MONTHS


def build_existing_well_cashflow(
    decline_result: dict,
    prod_df_for_well: pd.DataFrame,
    cfg: dict,
    days_per_month: float = 30.44,
) -> np.ndarray:
    """
    Build a monthly net cash flow array for an existing well.

    Uses actual production for past months, then the fitted decline
    projection for future months.

    Returns a 1-D array of net monthly cash flows ($).
    """
    if not decline_result.get("success"):
        return np.array([])

    # ── Historical production (actual) ────────────────────────────────────
    hist = prod_df_for_well.sort_values("prod_date").copy()
    hist_filtered = hist[hist["days_on"].fillna(0) >= 15]
    if not hist_filtered.empty:
        hist = hist_filtered
    oil_hist   = hist["oil_bbl"].fillna(0).values
    gas_hist   = hist["gas_mcf"].fillna(0).values
    water_hist = hist["water_bbl"].fillna(0).values if "water_bbl" in hist.columns else np.zeros_like(oil_hist)

    rev_hist  = calc_monthly_revenue(oil_hist, gas_hist, cfg)
    loe_hist  = (
        oil_hist   * cfg.get("loe_oil",   0.0) +
        gas_hist   * cfg.get("loe_gas",   0.0) +
        water_hist * cfg.get("loe_water", 0.0) +
        cfg.get("loe_fixed", 0.0)
    )
    net_hist  = rev_hist["net_revenue"] - loe_hist

    # ── Projected production (future) ─────────────────────────────────────
    qi  = decline_result["qi"]
    Di  = decline_result["Di_monthly"]
    b   = decline_result["b"]
    n_proj = MAX_PROJECTION_MONTHS

    oil_proj = project_monthly_volumes(qi, Di, b, n_proj, days_per_month)
    # Estimate gas from historical GOR
    if hist["oil_bbl"].sum() > 0:
        gor = hist["gas_mcf"].sum() / hist["oil_bbl"].sum()
    else:
        gor = 1.5  # MCF/BBL default
    gas_proj = oil_proj * gor
    # Estimate water from historical WOR
    oil_sum = hist["oil_bbl"].sum()
    if "water_bbl" in hist.columns and oil_sum > 0:
        wor = hist["water_bbl"].fillna(0).sum() / oil_sum
    else:
        wor = cfg.get("wor", 1.5)
    water_proj = oil_proj * wor

    rev_proj  = calc_monthly_revenue(oil_proj, gas_proj, cfg)
    loe_proj  = (
        oil_proj   * cfg.get("loe_oil",   0.0) +
        gas_proj   * cfg.get("loe_gas",   0.0) +
        water_proj * cfg.get("loe_water", 0.0) +
        cfg.get("loe_fixed", 0.0)
    )
    net_proj  = rev_proj["net_revenue"] - loe_proj

    # Trim trailing zeros
    last_nonzero = np.nonzero(oil_proj)[0]
    if len(last_nonzero):
        net_proj = net_proj[: last_nonzero[-1] + 1]

    return np.concatenate([net_hist, net_proj])


def build_undrilled_well_cashflow(
    oil_profile: np.ndarray,
    gas_profile: np.ndarray,
    water_profile: np.ndarray,
    cfg: dict,
    formation: str,
    days_per_month: float = 30.44,
) -> np.ndarray:
    """
    Build a monthly cash flow for an undrilled well using 3-stream type curve profiles.

    oil_profile, gas_profile, water_profile:
        Monthly volumes (BBL or MCF) normalized to 10,000 ft lateral.
        Generated by generate_type_curve_profile() in type_curve.py.

    D&C capex is prepended as a negative cash flow at index 0.
    Returns a 1-D array starting with -dc_cost.
    """
    lateral = cfg.get("lateral_length", 10_000)
    scale   = lateral / 10_000.0

    def _scale(arr):
        return np.nan_to_num(np.asarray(arr, dtype=float), nan=0.0) * scale

    oil_bbl   = _scale(oil_profile)
    gas_mcf   = _scale(gas_profile)
    water_bbl = _scale(water_profile)

    # Align lengths
    n = max(len(oil_bbl), len(gas_mcf), len(water_bbl))
    def _pad(a):
        if len(a) < n:
            return np.concatenate([a, np.zeros(n - len(a))])
        return a[:n]
    oil_bbl, gas_mcf, water_bbl = _pad(oil_bbl), _pad(gas_mcf), _pad(water_bbl)

    rev  = calc_monthly_revenue(oil_bbl, gas_mcf, cfg)
    loe  = (
        oil_bbl   * cfg.get("loe_oil",   0.0) +
        gas_mcf   * cfg.get("loe_gas",   0.0) +
        water_bbl * cfg.get("loe_water", 0.0) +
        cfg.get("loe_fixed", 0.0)
    )
    net = rev["net_revenue"] - loe

    # Trim trailing zeros
    nonzero = np.nonzero(oil_bbl)[0]
    if len(nonzero):
        net = net[: nonzero[-1] + 1]

    dc_cost = cfg["dc_costs"].get(formation, 10.0) * 1_000_000
    return np.concatenate([[-dc_cost], net])
